    private void fillTable() {
        ArrayList<Course> courses = cDao.getCourseStdID(stdId);
        DefaultTableModel dtm = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        String titulos[] = {"Course"};
        dtm.setColumnIdentifiers(titulos);
        for (Course c : courses) {
            Object[] fila = new Object[]{
                c.getName()
            };

            dtm.addRow(fila);
        }
        this.tableCoursesUser.setModel(dtm);
    }

    private void fillTableUser() {
        Student student = sDao.getStudent(stdId);
        this.userName.setText(student.getName());
        this.userLastName.setText(student.getLastNames());
        this.userUserName.setText(student.getUserName());
        this.userMail.setText(student.getEmail());
        this.userPw.setText(student.getPassword());

    }

